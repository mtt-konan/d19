def _build_triple_trace(
    p: int,
    q: int,
    r: int,
    seeds_raw: list[tuple[Fraction, Fraction, Fraction]],
    max_steps: int,
    min_rational: int,
    inside_only: bool,
    seen_canonical: set[tuple[Fraction, Fraction]],
) -> ECTripleTrace:
    seed_records = [
        ECSeedRecord(
            seed_index=index,
            a=seed_k.numerator,
            b=seed_k.denominator,
            k=seed_k,
            dB=dB,
            dD=dD,
        )
        for index, (seed_k, dB, dD) in enumerate(seeds_raw)
    ]

    ec = QuarticEC(p, q, r)
    seed_branches: list[ECSeedBranch] = []
    for seed in seed_records:
        for t in ec.t_from_k_dB(seed.k, seed.dB):
            E = ec.E_from_t_dD(t, seed.dD)
            if ec.on_curve(t, E):
                seed_branches.append(ECSeedBranch(seed.seed_index, t, E))
                if E != 0:
                    seed_branches.append(ECSeedBranch(seed.seed_index, t, -E))

    if not seed_branches:
        return ECTripleTrace(p=p, q=q, r=r, seeds=seed_records, nodes=[], edges=[], candidates=[])

    orbit_trace = ec.expand_orbit_trace(seed_branches, max_steps=max_steps)

    seen_k: set[Fraction | None] = set()
    candidates: list[ECCandidateRecord] = []

    def add_candidate(
        source_kind: str,
        source_seed_index: int | None,
        source_node_index: int | None,
        k: Fraction | None,
    ) -> None:
        base_status, x, y, point, canonical = _evaluate_candidate(
            p, q, r, k, inside_only, min_rational
        )
        status = base_status
        if k in seen_k:
            status = "k_duplicate"
        else:
            seen_k.add(k)
            if base_status == "candidate_ok" and canonical is not None:
                if canonical in seen_canonical:
                    status = "d4_duplicate"
                else:
                    status = "accepted"
                    seen_canonical.add(canonical)

        candidates.append(
            ECCandidateRecord(
                candidate_index=len(candidates),
                source_kind=source_kind,
                source_seed_index=source_seed_index,
                source_node_index=source_node_index,
                k=k,
                status=status,
                x=x,
                y=y,
                point=point,
                canonical_xy=canonical,
            )
        )

    for seed in seed_records:
        add_candidate(
            source_kind="seed",
            source_seed_index=seed.seed_index,
            source_node_index=orbit_trace.seed_primary_nodes.get(seed.seed_index),
            k=seed.k,
        )

    for node_index in orbit_trace.active_node_indices:
        node = orbit_trace.nodes[node_index]
        add_candidate(
            source_kind="orbit",
            source_seed_index=None,
            source_node_index=node.node_index,
            k=ec.k_from_t(node.t),
        )

    return ECTripleTrace(
        p=p,
        q=q,
        r=r,
        seeds=seed_records,
        nodes=orbit_trace.nodes,
        edges=orbit_trace.edges,
        candidates=candidates,
    )


def ec_search(
    max_m: int = 30,
    max_k_num: int = 200,
    max_k_den: int = 100,
    max_steps: int = 20,
    min_rational: int = 3,
    inside_only: bool = False,
    progress: bool = True,
    xp=None,
    store: Any | None = None,
) -> list[RationalPoint]:
    """Search for rational-distance points using the elliptic-curve method.

    Algorithm:
    1. Generate all primitive Pythagorean triples (p, q, r) up to max_m.
    2. For each triple, find seeds (k, dB, dD) with dA=k, dB, dD all rational
       by brute force over k=a/b with a≤max_k_num, b≤max_k_den.
    3. Convert seeds to rational points on the quartic curve E²=F(t).
    4. Expand the EC orbit using chord-tangent steps (up to max_steps).
    5. For each new k in the orbit, check if the point has min_rational
       rational distances (including dC via the parallelogram law).
    6. Deduplicate by D4 symmetry.

    Parameters
    ----------
    xp : array backend for GPU acceleration.  Pass the cupy module or a
         PyTorch wrapper (as returned by detect_backend) to use the GPU for
         the seed-finding step.  None (default) uses numpy on CPU.

    Returns a list of RationalPoints sorted by denominator.
    """
    try:
        from tqdm import tqdm

        _tqdm = tqdm
    except ImportError:

        def _tqdm(it, **kw):
            return it

    triples = primitive_pythagorean_triples(max_m)

    # Build coprime arrays once; upload to device if a real GPU backend given.
    _use_gpu = xp is not None and xp is not np
    a_cpu, b_cpu = _build_coprime_arrays(max_k_num, max_k_den)
    if _use_gpu:
        a_dev = xp.array(a_cpu.tolist(), dtype=xp.int64)
        b_dev = xp.array(b_cpu.tolist(), dtype=xp.int64)
    else:
        a_dev = b_dev = None  # not used

    # Int64 safety bound — same logic as in find_seeds_for_triple
    coeff = max_k_num + max_k_den
    safe_r_max = _INT64_SAFE_HALF // coeff if coeff > 0 else 10**18

    results = store.existing_points() if store is not None else []
    seen_canonical: set[tuple[Fraction, Fraction]] = {
        canonical_xy(point.x, point.y) for point in results
    }

    it = _tqdm(triples, desc="EC search", unit="triple", disable=not progress)
    for triple_index, (p, q, r) in enumerate(it):
        if store is not None and (p, q, r) in store.processed_triples:
            continue

        # Seed finding — GPU, numpy, or Python fallback based on safety
        if _use_gpu and r <= safe_r_max:
            raw = _find_seeds_gpu(xp, p, q, r, a_dev, b_dev, inside_only)
            seeds_raw = _seeds_raw_to_fractions(raw, r)
        else:
            seeds_raw = find_seeds_for_triple(
                p,
                q,
                r,
                max_k_num,
                max_k_den,
                inside_only,
                _a_arr=a_cpu,
                _b_arr=b_cpu,
            )

        if not seeds_raw:
            if store is not None:
                store.record_triple(
                    triple_index,
                    ECTripleTrace(p=p, q=q, r=r, seeds=[], nodes=[], edges=[], candidates=[]),
                )
            continue

        if store is not None:
            trace = _build_triple_trace(
                p=p,
                q=q,
                r=r,
                seeds_raw=seeds_raw,
                max_steps=max_steps,
                min_rational=min_rational,
                inside_only=inside_only,
                seen_canonical=seen_canonical,
            )
            store.record_triple(triple_index, trace)
            results.extend(trace.accepted_points())
            continue

        ec = QuarticEC(p, q, r)

        curve_seeds: list[tuple[Fraction, Fraction]] = []
        for k, dB, dD in seeds_raw:
            for t in ec.t_from_k_dB(k, dB):
                E = ec.E_from_t_dD(t, dD)
                if ec.on_curve(t, E):
                    curve_seeds.append((t, E))
                    if E != 0:
                        curve_seeds.append((t, -E))

        if not curve_seeds:
            continue

        k_values_from_orbit = ec.expand_orbit(curve_seeds, max_steps=max_steps)
        seed_k_values = [k for k, _, _ in seeds_raw]
        all_k_values = list(dict.fromkeys(seed_k_values + k_values_from_orbit))

        for k in all_k_values:
            pt = _make_ec_point(p, q, r, k, inside_only)
            if pt is None:
                continue
            if pt.rational_count < min_rational:
                continue

            cx, cy = canonical_xy(pt.x, pt.y)
            if (cx, cy) in seen_canonical:
                continue
            seen_canonical.add((cx, cy))
            results.append(pt)

    results.sort(key=lambda pt: pt.denominator)
    return results

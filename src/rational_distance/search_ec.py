"""Elliptic-curve guided search for rational-distance solutions.

Layer 2 of the search strategy: given the parametric seeds found by
brute force, this module extends them along the rational orbit of the
associated quartic elliptic curve to find additional solutions that
lie outside the brute-force range.

Mathematical background
-----------------------
For a primitive Pythagorean triple (p, q, r) with p²+q²=r², consider
the point P=(x,y) = (kp/r, kq/r).  Then:

    dA = k  (always rational for rational k)
    dB² = k² - 2k(p/r) + 1
    dD² = k² - 2k(q/r) + 1
    dC² = dB² + dD² - dA²  (parallelogram law: dA²+dC²=dB²+dD²)

Parametrize the conic dB²=k²-2kp/r+1 via slope t through (k,dB)=(0,1):

    k(t) = 2(t + p/r) / (1 - t²)
    dB(t) = (1 + t² + 2tp/r) / (1 - t²)

Substituting into dD and letting E = r·dD·(1-t²) gives the quartic:

    E² = F(t)  where
    F(t) = r²t⁴ + 4qrt³ + (2r²+4pq)t² + 4r(2p-q)t + (4p²-4pq+r²)

Rational points on this curve (hyperelliptic genus-1 curve over ℚ) form
a group under the chord-tangent law.  Starting from a *seed* — a rational
point discovered by brute force — new rational points are generated by
applying tangent and secant steps.  Each new rational t yields a new
rational k (and thus a new candidate point), which is then checked for
the full 3- or 4-vertex rationality condition.
"""

